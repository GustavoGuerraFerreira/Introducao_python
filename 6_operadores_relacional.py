# > < == !=
operador_relacional =  2 > 3

operador_relacional_maior =  5 > 2 
operador_relacional_menor =  7 < 4  
operador_relacional_maior_igual =  8 >= 8
operador_relacional_menor_igual =  9 <= 7 
operador_relacional_igual =  5 == 5 
operador_relacional_diferente =  4 != 4 

print(operador_relacional)  #False
print(operador_relacional_maior) #True
print(operador_relacional_menor) #False
print(operador_relacional_maior_igual) #True
print(operador_relacional_menor_igual) #False
print(operador_relacional_igual) #True
print(operador_relacional_diferente) #False
